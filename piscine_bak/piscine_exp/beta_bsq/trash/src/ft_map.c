#include "bsq.h"

char	*ft_map(char *in)
{
	int		i;
	int		j;
	char	*map;

	i = 0;
	j = 0;
	while (in[i] != '\n')
		i++;
	i++;
	map = (char *)malloc(ft_strlen(in + i) + 1);
	while (in[i])
	{
		map[j] = in[i];
		i++;
		j++;
	}
	map[j] = '\0';
	return (map);
}
