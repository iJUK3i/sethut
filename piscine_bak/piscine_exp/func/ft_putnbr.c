int		ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb < 0 && nb * -1 != nb)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb % 10 != nb)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 > 0 ? nb % 10 + '0' : nb % 10 * (-1) + '0');
}
