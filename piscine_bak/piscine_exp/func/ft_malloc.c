#include "bsq.h"

char	*ft_malloc(int i, char *str)
{
	char	*str2;
	int		j;

	str2 = (char*)malloc(i + 200000);
	j = 0;
	while (j <= i)
	{
		str2[j] = str[j];
		j++;
	}
	free(str);
	return (str2);
}
