#include <unistd.h>

int		main(int ac, char **av)
{
	int x;
	char *o;

	o = av[1];
	if (ac >= 2)
	{
		while (o != '\0')
		{
			if (*o == 'a')
			{
				write (1, "a", 1);
				break;
			}
			o++;
		}
	}
	else if (ac < 2)
	{
		write(1, "a", 1);
		write(1, "\n", 1);
	}
	return (0);
}



