#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putchar (char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + 48);
}

int	main(void)
{
	ft_putchar('\n');
	ft_putnbr(27);
	ft_putnbr(-2);
	ft_putchar('\n');
	ft_putnbr(9);
	ft_putchar('\n');
	return (0);
}
